console.log(sumIfNumber(1,2,'f','4',5,2));
console.log(sumIfNumber('m', 'a', 'r', 'i', 'n', 'a'));
console.log(sumIfNumberArrow(1, 2, 5, 2));
console.log(sumIfNumberArrow(1,2,'f','4',5,2));
console.log(sumIfNumberArrow('m', 'a', 'r', 'i', 'n', 'a'));
